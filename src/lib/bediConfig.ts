import { SEASON_COEFFICIENTS } from "./seasonFactors";

/**
 * BEDI тохиргооны тогтмолууд — судалгааны үндэслэлтэй.
 *
 * Эх сурвалж: В.Төмөрбаатар, "Нас, өндөр, жингийн хамаарлаар хүний биеийн
 * өвөрчлөлийг тодорхойлох нь" ("Төмөрбаатар Оточ" эмнэлэг, 2023–2025 оны
 * 400 өвчний түүхийн судалгаа):
 *   - Хазайлт = бодит жин ба байх ёстой хэвийн жингийн зөрүү
 *   - Түүврийн дундаж хазайлт Mean = +0.0133, стандарт хазайлт SD = 0.2006
 *   - SD-д суурилсан босго утгууд эмнэлзүйн оноштой 81% яг таарсан (χ², p<0.01)
 *
 * Эдгээр тогтмолыг өөрчлөхийн өмнө eзэмшигчийн (Тб оточ) баталгаажуулалт
 * шаардлагатай. Ирээдүйд админ панелаас тохируулах боломжтой болгохоор
 * бүгдийг энэ нэг файлд төвлөрүүлэв.
 */

/** Судалгааны түүврийн дундаж хазайлт (Mean) */
export const BEDI_MEAN = 0.0133;

/** Судалгааны стандарт хазайлт (SD) */
export const BEDI_SD = 0.2006;

/**
 * "Тэнцвэртэй" бүсийн өргөн (±k·SD). 1.5·SD ≈ ±0.30 — өмнөх хувилбарын
 * ±0.3 босготой залгамж чанартай.
 */
export const BALANCED_BAND_SDS = 1.5;

/** Тэнцвэртэй бүсийн доод/дээд босго (хазайлтын утгаар) */
export const BEDI_THRESHOLDS = {
  khiiBelow: BEDI_MEAN - BALANCED_BAND_SDS * BEDI_SD, // ≈ −0.29
  sharBadganAbove: BEDI_MEAN + BALANCED_BAND_SDS * BEDI_SD, // ≈ +0.31
} as const;

/**
 * Хэвийн жингийн лавлагаа BMI (ДЭМБ-ын хэвийн мужийн дунд утга).
 * Байх ёстой жин = REFERENCE_BMI · өндөр² · Kt^KT_WEIGHT_EXPONENT
 */
export const REFERENCE_BMI = 22;

/**
 * Махбодийн Kt коэффициент байх ёстой жинд нөлөөлөх зэрэг (0–1).
 * 0.5 үед: Хий (Kt 0.70) → байх ёстой жин −16%, Бадган (Kt 1.50) → +22%.
 * (Kt-г шууд үржүүлбэл Бадганы "хэвийн" жин +50% болж бодит бус.)
 */
export const KT_WEIGHT_EXPONENT = 0.5;

/**
 * Улирлын нөлөөллийн зэрэг (0–1). Улирлын илч жилийн дундажтай
 * харьцуулагдаж (s/S̄)^γ хэлбэрээр хазайлтад тусна: γ=0.5 үед өвөл ≈ +17%,
 * зун ≈ −17% налуу өгнө — улирал мэдэгдэхүйц ч жингийн дохиог дарахгүй.
 */
export const SEASON_TILT_EXPONENT = 0.5;

/** Улирлын коэффициентийн жилийн дундаж (S̄) */
export const MEAN_SEASON_COEFFICIENT =
  (SEASON_COEFFICIENTS.WINTER +
    SEASON_COEFFICIENTS.SPRING +
    SEASON_COEFFICIENTS.SUMMER +
    SEASON_COEFFICIENTS.AUTUMN) /
  4;

/** Хазайлт болон индексийн нарийвчлал (таслалаас хойших орон) */
export const BEDI_PRECISION = 2;

/** Нэг газар тодорхойлсон тоймлолт */
export function roundBedi(value: number): number {
  const f = 10 ** BEDI_PRECISION;
  return Math.round(value * f) / f;
}
